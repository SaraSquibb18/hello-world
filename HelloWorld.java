public class HelloWorld
{
    /**
     * Say hello to the world
     */
    public void sayHello()
    {
        System.out.println("Hello, World!");
    }
    
    /**
     * Say hello to someone by name
     * @param name
     */
    public void sayHello(String name)
    {
        System.out.println("Hello, " + name + "!");
    }       
}
